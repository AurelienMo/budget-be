namespace App\Domain\BankAccount\ListAccounts;

use App\Domain\InputInterface;
use App\Entity\User;

/**
 * Class ListAccountsInput
 */
class ListAccountsInput implements InputInterface
{
    /**
     * @var User|null
     */
    protected $user;

    /**
     * @return User|null
     */
    public function getUser(): ?User
    {
        return $this->user;
    }

    /**
     * @param User|null $user
     */
    public function setUser(?User $user): void
    {
        $this->user = $user;
    }
}
